// Helper function to generate additional vehicles
function generateAdditionalVehicles(start: number, end: number): Vehicle[] {
  const makes = ['Honda', 'Hyundai', 'Maruti Suzuki', 'Tata', 'Kia', 'Mahindra', 'Toyota', 'Ford', 'Renault', 'Nissan']
  const models = ['City', 'Verna', 'Swift', 'Nexon', 'Seltos', 'XUV300', 'Innova', 'EcoSport', 'Duster', 'Kicks']
  const colors = ['White', 'Black', 'Silver', 'Red', 'Blue', 'Grey']
  const cities = [
    { city: 'Mumbai', state: 'Maharashtra' },
    { city: 'Delhi', state: 'Delhi' },
    { city: 'Bangalore', state: 'Karnataka' },
    { city: 'Chennai', state: 'Tamil Nadu' },
    { city: 'Hyderabad', state: 'Telangana' },
  ]

  const additionalVehicles: Vehicle[] = []

  for (let i = start; i <= end; i++) {
    const make = makes[i % makes.length]
    const model = models[i % models.length]
    const year = 2018 + (i % 6)
    const price = 500000 + i * 50000
    const bodyType: 'Sedan' | 'SUV' | 'Hatchback' | 'Luxury' | 'Sports' | 'Electric' =
      i % 3 === 0 ? 'SUV' : i % 2 === 0 ? 'Sedan' : 'Hatchback'

    additionalVehicles.push({
      id: `veh_${String(i).padStart(3, '0')}`,
      make,
      model,
      variant: 'VX',
      year,
      slug: `${make.toLowerCase().replace(' ', '-')}-${model.toLowerCase()}-${year}`,
      price,
      emi: Math.round(price * 0.0152),
      images: ['https://images.unsplash.com/photo-1552519507-da3b142c6e3d?w=800'],
      thumbnailImage: 'https://images.unsplash.com/photo-1552519507-da3b142c6e3d?w=400',
      mileage: 20000 + i * 1000,
      fuelType: i % 3 === 0 ? 'Diesel' : 'Petrol',
      transmission: i % 2 === 0 ? 'Automatic' : 'Manual',
      ownership: 'First',
      registrationYear: year,
      registrationState: 'MH',
      bodyType,
      color: colors[i % colors.length],
      seatingCapacity: bodyType === 'SUV' ? 7 : 5,
      engineCC: 1500,
      power: '110 bhp',
      torque: '200 Nm',
      fuelEfficiency: '18 kmpl',
      location: cities[i % cities.length],
      seller: {
        id: `seller_${String((i % 10) + 1).padStart(3, '0')}`,
        name: 'Auto Dealer',
        type: 'Dealer',
        rating: 4.0 + (i % 10) / 10,
        reviewCount: 100 + i * 5,
      },
      features: ['Touchscreen', 'Alloy Wheels', 'Rear Camera'],
      isCertified: i % 2 === 0,
      isNewArrival: i % 5 === 0,
      condition: {
        accidentFree: true,
        serviceHistory: true,
        warrantyAvailable: i % 3 === 0,
        overallScore: 7.5 + (i % 15) / 10,
      },
      description: `Well-maintained ${make} ${model} in good condition.`,
      rating: 4.0 + (i % 10) / 10,
      views: 500 + i * 50,
      postedDate: '2025-11-10',
      tags: i % 2 === 0 ? ['Certified'] : [],
    })
  }

  return additionalVehicles
}

// Export helper functions
export const getVehicleById = (id: string): Vehicle | undefined => {
  return vehicles.find((vehicle) => vehicle.id === id)
}

export const getVehicleBySlug = (slug: string): Vehicle | undefined => {
  return vehicles.find((vehicle) => vehicle.slug === slug)
}

export const getFeaturedVehicles = (count: number = 6): Vehicle[] => {
  return vehicles.filter((v) => v.isCertified && v.rating >= 4.5).slice(0, count)
}

export const getNewArrivals = (count: number = 8): Vehicle[] => {
  return vehicles.filter((v) => v.isNewArrival).slice(0, count)
}
